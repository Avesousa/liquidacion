package Clases;

public class DiasMesAnio {
    
    public String dia;
    public int diaEnt;
    public String mes;
    public int anio;
    public int mesEnt;

    public DiasMesAnio(String dia, int diaEnt, String mes, int mesEnt, int anio) {
        this.dia = dia;
        this.diaEnt = diaEnt;
        this.mes = mes;
        this.mesEnt = mesEnt;
        this.anio = anio;
        
    }
    
    
        
}
